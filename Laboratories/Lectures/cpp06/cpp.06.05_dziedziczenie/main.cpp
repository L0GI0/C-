#include <iostream>

class CShape
{
public:
	double GetX() const;
	double GetY() const;
	void SetX(double x);
	void SetY(double y);
	double Pole()
	{ return 0.0; }

	double x,y;
};

/////////////////////////////////////////////////////////////////////////
inline double CShape::GetX() const
{
	return x;
}

/////////////////////////////////////////////////////////////////////////
inline double CShape::GetY() const
{
	return y;
}

/////////////////////////////////////////////////////////////////////////
inline void CShape::SetX(double x)
{
	this->x = x;
}

/////////////////////////////////////////////////////////////////////////
inline void CShape::SetY(double y)
{
	this->y = y;
}

/////////////////////////////////////////////////////////////////////////
class CRrec : public CShape
{
public:
	double Pole();
	void rys(void);
};

/////////////////////////////////////////////////////////////////////////
inline double CRrec::Pole()
{
	return x*y;
}

/////////////////////////////////////////////////////////////////////////
inline void CRrec::rys(void)
{
	std::cout << "Rysuj CRrec\n";
}

/////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////
int main(int argc, char * argv[])
{
	using namespace std;
	CShape aShape;
	CRrec bRec;

	cout << "Pole CShape = " << aShape.Pole() << endl;

	bRec.SetX(5.0);
	bRec.SetY(4.0);
	cout << "Pole CRrec = " << bRec.Pole() << endl;
	cout << "Pole CRec, ale wywolana funkcja z CShape = " << bRec.CShape::Pole() << endl;
}
